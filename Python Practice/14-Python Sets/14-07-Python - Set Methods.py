#Set Methods

#Set methods are built-in functions that can be used to manipulate sets in Python.
#Some of the most commonly used set methods include:
#add(), update(), remove(), discard(), pop(), clear(), union(), intersection(), difference(), symmetric_difference()
#add() - Adds an element to the set.
#update() - Adds multiple elements to the set.
#remove() - Removes a specified element from the set. Raises a KeyError if the element is not found.
#discard() - Removes a specified element from the set. Does not raise an error if the element is not found.
#pop() - Removes and returns an arbitrary element from the set. Raises a KeyError if the set is empty.
#clear() - Removes all elements from the set.
#union() - Returns a new set that is the union of two or more sets.
#intersection() - Returns a new set that is the intersection of two or more sets.
#difference() - Returns a new set that is the difference of two sets.
#symmetric_difference() - Returns a new set that is the symmetric difference of two sets.
#symmetric_difference_update() - Updates the set with the symmetric difference of itself and another set.
#intersection_update() - Updates the set with the intersection of itself and another set.
#difference_update() - Updates the set with the difference of itself and another set.
#isdisjoint() - Returns True if two sets have no elements in common, otherwise returns False.
#issubset() - Returns True if a set is a subset of another set, otherwise returns False.
#issuperset() - Returns True if a set is a superset of another set, otherwise returns False.
#copy() - Returns a shallow copy of the set.
