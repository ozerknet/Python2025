
txt = "Hello London"

print(txt.capitalize()) # Capitalizes the first character of the string
print(txt.casefold()) # Converts the string into lower case
print(txt.center(20)) # Centers the string in a 20 character wide space
print(txt.count("o")) # Returns the number of times a specified value occurs in a string
print(txt.encode()) # Returns an encoded version of the string
print(txt.endswith("London")) # Returns true if the string ends with the specified value
print(txt.expandtabs(2)) # Sets the tab size to 2 spaces
print(txt.find("o")) # Searches the string for a specified value and returns the position of where it was found
print(txt.format()) # Formats specified values in a string
print(txt.index("o")) # Searches the string for a specified value and returns the position of where it was found    
print(txt.isalnum()) # Returns True if all characters in the string are alphanumeric
print(txt.isalpha()) # Returns True if all characters in the string are in the alphabet
print(txt.isascii()) # Returns True if all characters in the string are ascii characters
print(txt.isdecimal()) # Returns True if all characters in the string are decimals
print(txt.isdigit()) # Returns True if all characters in the string are digits
print(txt.isidentifier()) # Returns True if the string is an identifier
print(txt.islower()) # Returns True if all characters in the string are lower case
print(txt.isnumeric()) # Returns True if all characters in the string are numeric
print(txt.isprintable()) # Returns True if all characters in the string are printable
print(txt.isspace()) # Returns True if all characters in the string are whitespaces
print(txt.istitle()) # Returns True if the string follows the rules of a title
print(txt.isupper()) # Returns True if all characters in the string are upper case
print(txt.join("Hello")) # Joins the elements of an iterable to the end of the string
print(txt.ljust(20)) # Returns a left justified version of the string
print(txt.lstrip()) # Returns a left trim version of the string
print(txt.maketrans("o", "O")) # Returns a translation table to be used in translations
print(txt.partition("o")) # Returns a tuple where the string is parted into three parts
print(txt.replace("o", "O")) # Returns a string where a specified value is replaced with a specified value
print(txt.rfind("o")) # Searches the string for a specified value and returns the last position of where it was found
print(txt.rindex("o")) # Searches the string for a specified value and returns the last position of where it was found
print(txt.rsplit("o")) # Splits the string at the specified value and returns a list
print(txt.rstrip()) # Returns a right trim version of the string
print(txt.split("o")) # Splits the string at the specified value and returns a list
print(txt.splitlines()) # Splits the string at line breaks and returns a list
print(txt.startswith("Hello")) # Returns true if the string starts with the specified value
print(txt.strip()) # Returns a trimmed version of the string
print(txt.swapcase()) # Swaps cases, lower case becomes upper case and vice versa
print(txt.title()) # Returns a title version of the string
print(txt.upper()) # Converts a string into upper case
print(txt.zfill(20)) # Fills the string with a specified number of 0 values at the beginning until it reaches the specified length
print(txt.removeprefix("Hello")) # Removes the specified prefix from the string
print(txt.removesuffix("London")) # Removes the specified suffix from the string
print(txt.rsplit("o")) # Splits the string at the specified value and returns a list
print(txt.rsplit("o", 1)) # Splits the string at the specified value and returns a list, with a maximum of 1 split


