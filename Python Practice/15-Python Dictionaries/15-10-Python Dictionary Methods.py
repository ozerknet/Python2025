#Dictionary Methods

#Dictionary methods are built-in functions that can be used to manipulate dictionaries in Python.
#Some of the most commonly used dictionary methods include:
#clear(), copy(), fromkeys(), get(), items(), keys(), pop(), popitem(), setdefault(), update(), values()
#clear() - Removes all elements from the dictionary.
#copy() - Returns a shallow copy of the dictionary.
#fromkeys() - Returns a new dictionary with the specified keys and values.
#get() - Returns the value of the specified key. If the key does not exist, it returns None or a specified default value.
#items() - Returns a view object that displays a list of a dictionary's key-value tuple pairs.
#keys() - Returns a view object that displays a list of all the keys in the dictionary.
#pop() - Removes the specified key and returns its value. If the key does not exist, it raises a KeyError.
#popitem() - Removes and returns the last inserted key-value pair as a tuple. If the dictionary is empty, it raises a KeyError.
#setdefault() - Returns the value of the specified key. If the key does not exist, it inserts the key with a specified value.
#update() - Updates the dictionary with the specified key-value pairs. If the key already exists, it updates its value.
#values() - Returns a view object that displays a list of all the values in the dictionary
