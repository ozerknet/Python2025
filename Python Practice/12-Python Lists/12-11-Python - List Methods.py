#List Methods
#List methods are built-in functions that can be used to manipulate lists in Python.
#Some of the most commonly used list methods include:
#append(), extend(), insert(), remove(), pop(), clear(), index(), count(), sort(), reverse()
#append() - Adds an element to the end of the list.
#extend() - Adds multiple elements to the end of the list.
#insert() - Adds an element at a specified position in the list.
#remove() - Removes the first occurrence of a specified element from the list.
#pop() - Removes and returns the element at a specified position in the list. If no position is specified, it removes and returns the last element.
#clear() - Removes all elements from the list.
#index() - Returns the index of the first occurrence of a specified element in the list.
#count() - Returns the number of occurrences of a specified element in the list.
#sort() - Sorts the elements of the list in ascending order.
#reverse() - Reverses the order of the elements in the list.
